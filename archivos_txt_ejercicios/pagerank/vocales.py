
validar = True

while validar == True:

    
    letra = input("Ingrese una letra: ")

    if letra == 'a' or letra == 'A':
        
        validar = False
        

    if letra == 'e' or letra == 'E':
        
        validar = False
        

    if letra == 'i' or letra == 'I':
        
        validar = False
        
    
    if letra == 'o' or letra == 'O':
        
        validar = False
        
    
    if letra == 'u' or letra == 'U':
        
        validar = False

